bool program_module_from_buffer(uint8_t* buffer, uint32_t size, uint32_t flash_addr)
{
    if (buffer == 0 || size == 0)
        return false;

    // 1. Erase required sectors (4KB each)
    uint32_t sectors = (size + 4095) / 4096;

    for (uint32_t i = 0; i < sectors; i++)
    {
        uint32_t addr = flash_addr + (i * 4096);

        uint8_t addr_bytes[3] = {
            (uint8_t)(addr >> 16),
            (uint8_t)(addr >> 8),
            (uint8_t)addr
        };

        FLASH_gSectorErase(addr_bytes);
    }

    // 2. Program 256-byte pages
    for (uint32_t offset = 0; offset < size; offset += 256)
    {
        uint32_t page_size = (size - offset > 256) ? 256 : (size - offset);
        uint32_t addr = flash_addr + offset;

        

        FlashWrite(addr, &buffer[offset], page_size);
        // FlashWrite((uint32_t)0x000000, module_data, 256);

            Delay_ms(100);
        FlashRead(addr, &read_buf[offset], page_size);


            

    }

    return true;
}

